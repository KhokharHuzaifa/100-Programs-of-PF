//Q26-Write a program that input a number and find whether these numbers
//are equal or not using if else?

#include<iostream>
using namespace std;
int main(){
	int num1, num2;
	cout<<"Enter two numbers : ";
	cin>>num1>>num2;
	if(num1==num2){
		cout<<"Numbers are equal";
	}
	else{
		cout<<"Not equal";
	}
}
